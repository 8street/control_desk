void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	static int i;
    ADC_ChannelConfTypeDef sConfig = {0};

	if(hadc->Instance == ADC1)
	{
		DataToUSBhost.Rx = (uint16_t)HAL_ADC_GetValue(hadc) * 16U;
	}else if (hadc->Instance == ADC2)
	{

		DataToUSBhost.Ry = (uint16_t)HAL_ADC_GetValue(hadc) * 16U;

	}else if(hadc->Instance == ADC3)
	{

		if (i%2 == 0)
		{
			DataToUSBhost.Slider = (uint16_t)HAL_ADC_GetValue(hadc) * 16U;

		    sConfig.Channel = ADC_CHANNEL_10;
		    sConfig.Rank = 1;
		    sConfig.SamplingTime = ADC_SAMPLETIME_3CYCLES;
		    if (HAL_ADC_ConfigChannel(hadc, &sConfig) != HAL_OK)
		    {
		      Error_Handler();
		    }
		}else
		{
			DataToUSBhost.Throttle = (uint16_t)HAL_ADC_GetValue(hadc) * 16;

		    sConfig.Channel = ADC_CHANNEL_11;
		    sConfig.Rank = 1;
		    sConfig.SamplingTime = ADC_SAMPLETIME_3CYCLES;
		    if (HAL_ADC_ConfigChannel(hadc, &sConfig) != HAL_OK)
		    {
		      Error_Handler();
		    }
		}
		i++;
	}
}
